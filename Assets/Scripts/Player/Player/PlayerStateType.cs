// 플레이어 FSM 상태를 정의하는 전역 열거형 (모든 FSM 스크립트에서 공통 사용)

// SOLID 원칙 중 SRP(단일 책임 원칙)에 따라 분리된 상태 열거형 정의 파일
// GC 최소화를 위해 상태 비교 시 문자열 대신 enum 값 사용
// 프로젝트 전반에서 사용되므로 네임스페이스는 지정하지 않음 (공용 접근 목적)

public enum PlayerStateType
{
    Idle,       // 기본 대기 상태
    Move,       // 이동 상태 (지면 걷기 또는 뛰기)
    Jump,       // 점프 상태 (공중 상승 중)
    Fish,       // 낚시 상태 (고정 자세 낚시 중)
    Build,      // 건축 상태 (건물 배치 중)
    Craft,      // 조합 상태 (UI 조합 또는 요리)
    Attack,     // 공격 상태 (무기 휘두르기 등)
    Dead,       // 사망 상태 (체력 0)
    ThrowHook,  // 갈고리 던지기 상태 (Q키 등으로 발동)
    Swim        // 수영 상태 (수중 진입 시 부력 상태)
}
